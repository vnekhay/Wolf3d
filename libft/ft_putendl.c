#include "libft.h"

void	ft_putendl(char const *s)
{
	size_t j;

	j = 0;
	if (s)
	{
		while (s[j] != '\0')
		{
			write(1, &s[j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
}
