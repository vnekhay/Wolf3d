#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	size_t j;

	j = 0;
	if (s)
	{
		while (s[j] != '\0')
		{
			write(fd, &s[j], 1);
			j++;
		}
		write(fd, "\n", 1);
	}
}
