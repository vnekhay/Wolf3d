#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t j;

	j = 0;
	if (s && f)
	{
		while (s[j] != '\0')
		{
			f(j, &s[j]);
			j++;
		}
	}
}
